  var Game = function(canvasId) {
    var canvas = document.getElementById(canvasId);
    var screen = canvas.getContext('2d');
    var gameSize = { x: canvas.width, y: canvas.height};

    //this.bodies = [new Player(gameSize)];
    this.player = new Player(gameSize);
    this.bodies = createInvaders(this).concat(this.player);
    this.invader_bullets = [];

    this.last_time_fired = Date.now();
    this.start_time = new Date();
    console.log('start time: ', this.start_time);

    var self = this;

    var tick = function() {
      self.update();
      self.check_status();
      self.draw(screen,gameSize);  
      requestAnimationFrame(tick);
    }

    tick();
  };

  Game.prototype = {
    check_status: function() {
      if(this.player.alive) {
        if(!this.player.active) {
          this.player.alive = false;
          end_time = new Date();
          console.log('end time: ', end_time);
          console.log('game duration in seconds: ', (end_time - this.start_time)/1000);
	} else {
	  this.player.active = false;
	}
      }
    },
    update: function() {
     var bodies = this.bodies;
     var not_colliding_with_anything = function(b1) {
       var bodies_colliding = bodies.filter(function(b2) {return collided(b1,b2);}); 
       return(bodies_colliding.length == 0);
     };

     this.bodies = this.bodies.filter(not_colliding_with_anything);
     for(i=0;i<this.bodies.length;i++){ 
       var response = this.bodies[i].update(this);
       if(response == 1) {
	 // this does not find/remove invader bullets that collide before leaving area
	 // so, now the loop after this one finds those and deletes them
	 var invader_bullet_index = -1;
	 invader_bullet_index = this.invader_bullets.indexOf(this.bodies[i]);
         this.invader_bullets.splice(invader_bullet_index, 1);
         this.bodies.splice(i,1);
       }
     }
     for(i=0;i<this.invader_bullets.length;i++) {
       // if bullet is no longer in bodies, then it has collided, so remove it
       var missing_bullet_index = -1;
       missing_bullet_index = this.bodies.indexOf(this.invader_bullets[i]);
       if(-1 == missing_bullet_index) {
	 console.log('************************* deleting bullet ********************************');
	 this.invader_bullets.splice(i,1);
       }
     }
     if(true === this.player.active) {
       this.player.update_random(this);
     }
    },
    draw: function(screen,gameSize) {
      screen.clearRect(0,0,gameSize.x,gameSize.y);
      for(i=0;i<this.bodies.length;i++) {
        drawRect(screen, this.bodies[i]);
      }
    },
    addInvaderBullet: function(bullet) {
      this.invader_bullets.push(bullet);
      //console.log('invader bullets: ', this.invader_bullets.length);
    },
    addBody: function(body) {
      //console.log('size: ' + this.bodies.length);
      this.bodies.push(body);
    },
    invadersBelow: function(invader) {
      return this.bodies.filter(function(b) {
	return (b instanceof Invader) && (b.center.y > invader.center.y) && (b.center.x - invader.center.x < invader.size.x);
      }).length > 0;
    }
  };

